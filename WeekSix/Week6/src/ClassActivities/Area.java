package WeekSix.Week6.src.ClassActivities;

public class Area {
    public static int calulateSquareArea(int length) {
        return length * length;
    }

    public static double calulateSquareArea(double length) {
        return length * length;
    }

    public static int calulateRectangleArea(int length, int width) {
        return length * width;
    }

    public static double calulateRectangleArea(double length, double width) {
        return length * width;
    }

    public static double calulateCircleArea(double r) {
        return Math.PI * r * r;
    }
}
